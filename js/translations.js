function changeLanguage(lang) {
    document.querySelectorAll('[data-key]').forEach(element => {
        const key = element.getAttribute('data-key');
        element.textContent = translations[lang][key] || element.textContent;
    });
    document.documentElement.lang = lang;
    const pageName = document.location.pathname.split('/').pop().split('.')[0] || "index";
    document.title = translations[lang][`page-title-${pageName}`] || translations[lang]["page-title-index"];
    localStorage.setItem('lang', lang);
}

// Attendre que le DOM soit complètement chargé avant d'exécuter le script
document.addEventListener('DOMContentLoaded', () => {
    let languageSelect = document.querySelector('#language-select');
    if (!languageSelect) return; // Si le sélecteur n'existe pas, arrêter

    let currentLang = localStorage.getItem('lang') || 'fr';
    languageSelect.value = currentLang;
    changeLanguage(currentLang);

    languageSelect.addEventListener('change', function() {
        currentLang = this.value;
        changeLanguage(currentLang);
    });
});